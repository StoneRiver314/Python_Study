# for文

# range(繰り返し回数)
for x in range(100):
    print("Hello World!")
# range(100)で100回ループ
# for の次の変数xにrangeの数字が代入される

# range(開始, 終了)
for x in range(1, 101):
    print("Hello World")
# 1から100までループする

# ragneの値
for x in range(5):
    # 5回ループ
    print(x)
    # x = 0 から x = 4まで


# rangeの値
for x in range(1, 6):
    # 5回ループ
    print(x)
    # x = 1 から x = 5まで

# range引数（増加量の指定）
for x in range(2, 15, 2):
    print("range引数 x = " + str(x))
    # x =2から14までの偶数
